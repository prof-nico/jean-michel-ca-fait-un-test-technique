cols=$(tput cols)

train=(
"                                       __________________________________             "
"                                      /                                  \\            "
"                                     |  BEEP BEEP ! JEAN MICHEL ÇA ROULE |            "
"                                      \\_________________  ______________/            "
"                                                        |/                            "
"               ________________________                                              "
"              |  ____________________  |                                             "
"              | |        _           | |                                             "
"              | |     __( )__        | |      <-- Jean-Michel avec                    "
"              | |    [(-___-)]_      | |          ses lunettes de soleil             "
"       _______| |_____\\__^__/_______| |_______                                      "
"      / _____ |       | /----\\ |       | _____ \\                                     "
"     / /     \\|_______|/______\\|_______|/     \\ \\                                    "
"    | |   _   |________________________|   _   | |                                    "
"    | |  / \\  |          ||||          |  / \\  | |                                    "
"    | | | O | |==========||||==========| | O | | |                                    "
"    | |  \\_/  |__________|____|__________  \\_/  | |                                    "
"     \\ \\_____/ //      \\\\______//      \\\\_____/ /                                     "
"      \\_______//        \\\\______//        \\_______/                                    "
"        {===}              {===}              {===}                                    "
"  ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~                    "
)

train_width=20

for (( pos=0; pos<cols+train_width; pos++ )); do
    clear
    for ligne in "${train[@]}"; do #tres
        printf "%${pos}s%s\n" "" "$ligne"
    done
    sleep 0.1
done
